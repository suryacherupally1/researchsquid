"""
Aggregation and reporting helpers for DRACO benchmark results.

Adapted from nsourlos/LLM_evaluation_framework — spider chart layout
and per-domain breakdown structure.
"""

from __future__ import annotations

import json
import statistics
from pathlib import Path


AXES = ["overall_score", "factual_accuracy", "breadth_depth", "presentation", "citation"]
AXIS_LABELS = {
    "overall_score": "Overall",
    "factual_accuracy": "Factual Accuracy",
    "breadth_depth": "Breadth & Depth",
    "presentation": "Presentation",
    "citation": "Citation Quality",
}


def _stats(values: list[float]) -> dict:
    if not values:
        return {"n": 0, "mean": 0.0, "median": 0.0, "std": 0.0, "min": 0.0, "max": 0.0}
    return {
        "n": len(values),
        "mean": round(statistics.mean(values), 2),
        "median": round(statistics.median(values), 2),
        "std": round(statistics.stdev(values) if len(values) > 1 else 0.0, 2),
        "min": round(min(values), 2),
        "max": round(max(values), 2),
    }


def build_summary(score_records: list[dict]) -> dict:
    """Aggregate score records into a summary dict."""
    all_overall = [r["overall_score"] for r in score_records]

    axes_data: dict[str, list[float]] = {
        "factual_accuracy": [],
        "breadth_depth": [],
        "presentation": [],
        "citation": [],
    }
    domain_scores: dict[str, list[float]] = {}

    for rec in score_records:
        for axis in axes_data:
            axes_data[axis].append(rec[axis])
        domain = rec["domain"]
        domain_scores.setdefault(domain, []).append(rec["overall_score"])

    return {
        "n_tasks": len(score_records),
        "overall": _stats(all_overall),
        "axes": {axis: _stats(vals) for axis, vals in axes_data.items()},
        "by_domain": {
            domain: _stats(vals) for domain, vals in sorted(domain_scores.items())
        },
    }


def write_markdown_report(
    summary: dict,
    score_records: list[dict],
    path: Path,
) -> None:
    """Write a human-readable markdown report."""
    lines = [
        "# DRACO Benchmark Report — ResearchSquid",
        "",
        f"**Tasks evaluated:** {summary['n_tasks']}",
        "",
        "## Overall Score",
        "",
        f"| Metric | Mean | Median | Std | Min | Max |",
        f"|--------|------|--------|-----|-----|-----|",
    ]

    def row(label: str, stats: dict) -> str:
        return (
            f"| {label} | {stats['mean']:.1f}% | {stats['median']:.1f}% "
            f"| {stats['std']:.1f} | {stats['min']:.1f}% | {stats['max']:.1f}% |"
        )

    lines.append(row("Overall", summary["overall"]))
    lines += [
        "",
        "## Evaluation Axes",
        "",
        f"| Axis | Mean | Median | Std | Min | Max |",
        f"|------|------|--------|-----|-----|-----|",
    ]
    for axis, stats in summary["axes"].items():
        lines.append(row(AXIS_LABELS[axis], stats))

    lines += [
        "",
        "## Per-Domain Breakdown",
        "",
        "| Domain | n | Mean | Median | Std |",
        "|--------|---|------|--------|-----|",
    ]
    for domain, stats in sorted(summary["by_domain"].items()):
        lines.append(
            f"| {domain} | {stats['n']} | {stats['mean']:.1f}% "
            f"| {stats['median']:.1f}% | {stats['std']:.1f} |"
        )

    lines += [
        "",
        "## Per-Task Scores",
        "",
        "| Task ID | Domain | Overall | Factual | Breadth | Presentation | Citation |",
        "|---------|--------|---------|---------|---------|--------------|----------|",
    ]
    for rec in sorted(score_records, key=lambda r: r["overall_score"], reverse=True):
        lines.append(
            f"| `{rec['task_id'][:8]}` | {rec['domain']} "
            f"| {rec['overall_score']:.1f}% "
            f"| {rec['factual_accuracy']:.1f}% "
            f"| {rec['breadth_depth']:.1f}% "
            f"| {rec['presentation']:.1f}% "
            f"| {rec['citation']:.1f}% |"
        )

    lines += [
        "",
        "---",
        "_Generated by ResearchSquid DRACO benchmark harness._",
    ]

    path.write_text("\n".join(lines), encoding="utf-8")


def load_scores(scores_path: Path) -> list[dict]:
    """Load a scores.jsonl file."""
    records = []
    with open(scores_path) as f:
        for line in f:
            line = line.strip()
            if line:
                records.append(json.loads(line))
    return records


def compare_runs(run_dirs: list[Path]) -> dict:
    """
    Compare multiple benchmark runs side by side.

    Args:
        run_dirs: List of result directories (each containing scores.jsonl).

    Returns:
        Dict mapping run label → summary.
    """
    comparison = {}
    for run_dir in run_dirs:
        scores_file = run_dir / "scores.jsonl"
        if not scores_file.exists():
            continue
        records = load_scores(scores_file)
        summary = build_summary(records)
        comparison[run_dir.name] = {
            "overall_mean": summary["overall"]["mean"],
            "axes": {axis: stats["mean"] for axis, stats in summary["axes"].items()},
            "n_tasks": summary["n_tasks"],
        }
    return comparison
